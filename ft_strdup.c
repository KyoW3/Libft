#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*str;
	int		i;

	i = 0;
	if (!(str = (char*)malloc(sizeof(*str) * (ft_strlen(src) + 1))))
		return (0);
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	free(str);
	return (str);
}
